    private void displayTable(){
        int selected = tabbedPane.getSelectedIndex();
        if(selected == -1){
            JOptionPane.showMessageDialog(tabbedPane, "Invalid index error", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        this.setTitle(tabbedPane.getTitleAt(selected) + " Records");
        try{
            switch(selected){
                case 0:
                    ps = conn.prepareStatement("SELECT * FROM COLLEGE");
                    rs = ps.executeQuery();
                    tblCollege.setModel(DbUtils.resultSetToTableModel(rs));
                    break;
                case 1:
                    ps = conn.prepareStatement("SELECT * FROM SUBJECT");
                    rs = ps.executeQuery();
                    tblSubject.setModel(DbUtils.resultSetToTableModel(rs));
                    break;
                default:
                    JOptionPane.showMessageDialog(tabbedPane, "Unhandled case", "Error", JOptionPane.ERROR_MESSAGE);
            }    
        }catch(Exception e){
            JOptionPane.showMessageDialog(tabbedPane, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
