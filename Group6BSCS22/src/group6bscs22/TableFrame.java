    private void btnCollegeAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCollegeAddMouseClicked
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(tabbedPane, "Do you really want to add?", 
                                                "Add record", JOptionPane.YES_NO_OPTION);
        if(response != 0){
            JOptionPane.showMessageDialog(tabbedPane, "Addition of record aborted", 
                                            "Abort", JOptionPane.YES_NO_OPTION);
            return;
        }
        try{
            ps = conn.prepareStatement("INSERT INTO COLLEGE "
                                     + "VALUES(?, ?, ?, ?, ?)");
            ps.setString(1, txtCollegeCode.getText());
            ps.setString(2, txtDescription.getText());
            ps.setString(3, txtActive.getText().toUpperCase());
            ps.setDate(4, Date.valueOf(txtDateOpened.getText()));
            ps.setDate(5, Date.valueOf(txtDateClosed.getText()));
            ps.executeUpdate();
        }catch(Exception e){
            JOptionPane.showMessageDialog(tabbedPane, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCollegeAddMouseClicked

