    private void tblMainMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMainMouseClicked
        // TODO add your handling code here:
        int row = tblMain.getSelectedRow();
        try{
            ps = conn.prepareStatement("SELECT * FROM " + listTables.getSelectedItem().toString());
            rs = ps.executeQuery();
            while(rs.next()){
                jTextField1.setText(tblMain.getValueAt(row, 0).toString());
                jTextField2.setText(tblMain.getValueAt(row, 1).toString());
                jTextField3.setText(tblMain.getValueAt(row, 2).toString());
                jTextField4.setText(tblMain.getValueAt(row, 3).toString());
                jTextField5.setText(tblMain.getValueAt(row, 4).toString());
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_tblMainMouseClicked

