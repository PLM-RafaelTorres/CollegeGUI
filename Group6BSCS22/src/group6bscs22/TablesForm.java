    private void showTable(){
        String currentTable = listTables.getSelectedItem().toString();
        try{
            ps = conn.prepareStatement("SELECT * FROM " + currentTable);
            rs = ps.executeQuery();
            ResultSetMetaData metadata = rs.getMetaData();
            tblMain.setModel(DbUtils.resultSetToTableModel(rs));
            
            lblField1.setText(metadata.getColumnName(1)
                    .replaceAll("_", " "));
            lblField2.setText(metadata.getColumnName(2)
                    .replaceAll("_", " "));
            lblField3.setText(metadata.getColumnName(3)
                    .replaceAll("_", " "));
            lblField4.setText(metadata.getColumnName(4)
                    .replaceAll("_", " "));
            lblField5.setText(metadata.getColumnName(5)
                    .replaceAll("_", " "));
            
        }catch(Exception e){
            System.out.println(e);
        }
    }
