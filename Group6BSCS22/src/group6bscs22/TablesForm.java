    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        int answer = JOptionPane.showConfirmDialog(this, "Do you really want to delete?", "Delete", JOptionPane.YES_NO_OPTION);
        if(answer != 0){
            JOptionPane.showMessageDialog(this, "Aborted update of record", "Abort",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            if(listTables.getSelectedItem().toString().equals("COLLEGE"))
            ps = conn.prepareStatement("DELETE FROM COLLEGE "
                + "WHERE COLLEGE_CODE = ?");

            else
            ps = conn.prepareStatement("DELETE FROM SUBJECT "
                + "WHERE SUBJECT_CODE = ?");

            ps.setString(1, jTextField1.getText());
            ps.executeUpdate();
            clearTextFields();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        int answer = JOptionPane.showConfirmDialog(this, "Do you really want to update?", "Update",JOptionPane.YES_NO_OPTION);
        if(answer != 0){
            JOptionPane.showMessageDialog(this, "Aborted update of record", "Abort",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            if(listTables.getSelectedItem().toString().equals("COLLEGE")){
                ps = conn.prepareStatement("UPDATE COLLEGE "
                    + "SET DESCRIPTION = ?, ACTIVE = ?, DATE_OPENED = ?, DATE_CLOSED = ? "
                    + "WHERE COLLEGE_CODE = ?");

                ps.setString(2, jTextField3.getText().toUpperCase());
                ps.setDate(3, Date.valueOf(jTextField4.getText()));
                ps.setDate(4, Date.valueOf(jTextField5.getText()));
            }
            else{
                ps = conn.prepareStatement("UPDATE SUBJECT "
                    + "SET DESCRIPTION = ?, UNITS = ?, COLLEGE_CODE = ?, ACTIVE = ? "
                    + "WHERE SUBJECT_CODE = ?");
                ps.setInt(2, Integer.valueOf(jTextField3.getText()));
                ps.setString(3, jTextField4.getText());
                ps.setString(4, jTextField5.getText().toUpperCase());
            }
            ps.setString(1, jTextField2.getText());
            ps.setString(5, jTextField1.getText());

            ps.executeUpdate();

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        int answer = JOptionPane.showConfirmDialog(this, "Do you really want to add?", "Add", JOptionPane.YES_NO_OPTION);
        if(answer != 0){
            JOptionPane.showMessageDialog(this, "Aborted insertion of record", "Abort",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            if(listTables.getSelectedItem().toString().equals("COLLEGE")){
                ps = conn.prepareStatement("INSERT INTO COLLEGE VALUES(?, ?, ?, ?, ?)");
                ps.setString(3, jTextField3.getText().toUpperCase());
                ps.setDate(4, Date.valueOf(jTextField4.getText()));
                ps.setDate(5, Date.valueOf(jTextField5.getText()));
            }
            else{
                ps = conn.prepareStatement("INSERT INTO SUBJECT VALUES (?, ?, ?, ?, ?)");
                ps.setInt(3, Integer.valueOf(jTextField3.getText()));
                ps.setString(4, jTextField4.getText());
                ps.setString(5, jTextField5.getText().toUpperCase());
            }

            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());

            ps.executeUpdate();

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddMouseClicked

